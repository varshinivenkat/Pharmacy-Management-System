    private void register_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_register_buttonActionPerformed
    // Get the values from the input fields
    String disid = id.getText();
    String disName = name.getText();
    String disRegion = region.getText();
    String discontactInfo = contact_info.getText();
    String password_dis = new String(password.getPassword());

    // Validate input fields
    if (disid.isEmpty() || disName.isEmpty() || disRegion.isEmpty() || discontactInfo.isEmpty() || password_dis.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Connect to the database
    try (Connection connection = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "scott", "tiger")) {
        // Construct the SQL INSERT statement
        String sql = "INSERT INTO distributors(distributor_id, name, region,phone_number, password) VALUES (?, ?, ?, ?, ?)";

        try (PreparedStatement statement = connection.prepareStatement(sql)) {
            // Set the values to be inserted
            statement.setString(1, disid);
            statement.setString(2, disName);
            statement.setString(3, disRegion);
            statement.setString(4, discontactInfo);
            statement.setString(5, password_dis);

            // Execute the SQL statement
            int rowsInserted = statement.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "A new distrubutor was inserted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to insert the distrubutor.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (SQLException ex) {
        if (ex.getSQLState().equals("23000")) { // SQLState for integrity constraint violation
            JOptionPane.showMessageDialog(this, "A distributor with the same ID already exists.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "An error occurred: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

       
    }//GEN-LAST:event_register_buttonActionPerformed

