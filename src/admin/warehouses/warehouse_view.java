    private void retrieveData() {
    String query = "SELECT m.medicine_id, m.name, wm.quantity " +
                       "FROM medicine m " +
                       "JOIN warehouse_details wm ON m.medicine_id = wm.medicine_id " +
                       "WHERE wm.warehouse_id = ?";

        try (Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "scott", "tiger");
             PreparedStatement stmt = conn.prepareStatement(query)) {

            stmt.setString(1, warehouseId);
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing data

            while (rs.next()) {
                String medicineId = rs.getString("medicine_id");
                String name = rs.getString("name");
                int quantity = rs.getInt("quantity");
                model.addRow(new Object[]{medicineId, name, quantity});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
}

