    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {                                      
    String shipid = SessionManager.getshipid();// Replace with actual shipment ID retrieval
   
    String updateStatusSQL = "UPDATE shipment SET status = 'DELIVERED' WHERE shipment_id = ?";
   
    try (Connection connection = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "scott", "tiger")) {
        try (PreparedStatement updateStatement = connection.prepareStatement(updateStatusSQL)) {
            updateStatement.setString(1, shipid);
           
            int rowsUpdated = updateStatement.executeUpdate();
           
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Shipment status updated to 'delivered'.", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No shipment found with ID: " + shipid, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "An error occurred: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }                                      


